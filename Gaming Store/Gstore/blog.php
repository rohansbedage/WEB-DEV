    <p><h2>Can you learn a language playing video games? What the research says </h2></p>
    <p class="blog-post-meta">November 1, 2020 by <a href="#">Rohan Bedage</a></p>

<p>Online gaming has become a concern for some parents in the past few years and there are worries children might become addicted, with negative effects on their socialisation. This has led some parents to think of creative ways to reduce gaming, including rationing the time children spend online.</p>
<p>It’s important to remember though, that not all the research into children playing video games paints a bleak picture. In fact, there is a growing body of research that suggests such worries might be unfounded and that gaming could be an incredibly useful educational tool which might actually make children more sociable, not less.</p>
<p>In the same way that many schools use other forms of technology to get students more excited about learning – such as interactive whiteboards and tablets – both of which seem popular with students, video games might also offer similar benefits.</p>
<h2>How gaming could help</h2>
<p>Language learning in particular seems a perfect place to try “gamified” classes. Some schools are already using Minecraft in French classes – the idea is that students work together to build a “learning zone” in the Minecraft space – finding new words to help them along the way.</p>
<p>
Indeed, James Paul Gee, a leading researcher in the area of video games as language learning tools, suggests that role-playing games such as The Elder Scrolls series or World of Warcraft, offer an ideal learning space for what he calls “at-risk” learners. In theory, there is just enough challenge, just enough support, just enough room for players to be themselves and, possibly most important, students have just enough “ownership” of the learning process.</p>

<p>“At-risk” language learners, by Gee’s definition, could be anyone. They may be learners with special educational needs, but equally they may also simply be learners who feel more vulnerable in a language classroom. Learning a language, after all, is a huge departure from some students’ comfort zones. Students, for example, can get nervous and inhibited in a classroom. Language learning researchers describe this as an “affective filter” – a fear of making a mistake and losing face literally affects how far a student joins in the class.</p>
<p>My research specifically looks at language learning – a subject area that, certainly in the UK, appears to be one that students seem to endure rather than necessarily enjoy. It builds on the ideas of Philip Hubbard, a leading researcher in the field of using technology to enhance language learning. He has previously suggested that while technology in classrooms is seen as useful there is no strategy for using it – and this is where my research comes in. What I’m aiming to do is find that strategy and try to answer the following questions: how video games might help, why some students might prefer playing a video game to being in class and what areas of language learning a teacher could improve with this technology.</p>
  
 <h2>The power of gaming</h2> 
  <p>Video games, especially massive multiplayer online role-playing games (MMOs) such as World of Warcraft, Final Fantasy XIV and Runescape tend to cut through all the reasons for someone to be self-conscious. Players have to communicate in real-time, with no opportunity to agonise over what to say, or how to speak perfectly.</p>
  <p>This real-time aspect of MMOs may sound terrifying for someone learning a language. But actually, a highly useful gamification study by Ian Glover, a lecturer in technology-enhanced learning at Sheffield Hallam University, found that learners in general have a high level of extrinsic motivation when they game. In other words, students really want to chase levelling up, bonuses and rewards, which they define as excelling within a gaming space.</p>
  <p>As a result, they may force themselves to become better at communicating so they can level up quicker – and this drive might go deeper still. Gamers are often encouraged to repeat levels several times, so that they can perform better.</p>
  
