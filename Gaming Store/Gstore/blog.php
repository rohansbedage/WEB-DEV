  <h2>Building connections</h2>
  <p>This is what the prominent motivation researcher, Zoltan Dörnyei, describes as “directed motivational currents”. The concept implies that motivation, for some students, may be driven entirely by their personal view of what success entails.</p>
  <p>This is important because if the commonly held theory that many students learn languages to “tick a box” needed for graduation is correct, then they may only be motivated to study just enough to pass. On the other hand, if success in the language classroom is aligned with success in a gaming space, then harnessing this drive may be a powerful way to foster continued interest in language learning and developing language skills.</p>
  <p>Video games may also have the potential to help learners develop more complex social skills. This view is inspired by the Russian philosopher Mikhail Bakhtin, who believed that truly meaningful communication came from negotiating cultural differences and finding solutions.</p>
  <p>This happens a lot in a video game, as players try to find their roles, but more significant is what the negotiations seem to lead to: relatedness – finding shared meaning and a sense of belonging. A study conducted in Japan found that players seemed to play MMOs mainly for the purpose of forming social connections.</p>
  <p>So will schools of the future all be learning languages through gaming? This remains to be seen, but given that the evidence suggests gaming can encourage social skills and teamwork – as well as incredible scope to share ideas and build knowledge – there may be a good argument for ditching the textbooks and logging into another world for a while.</p>
