    <h2 class="jumbotron-post-title">Gaming on the cloud, everywhere!</h2>
        <p class="blog-post-meta">November 1, 2020 by <a href="#">Rohan Bedage</a></p>

        <p>The Game Pass service uses “cloud gaming” technology. Whereas consoles provide the local computing hardware needed to play games, cloud gaming involves streaming games over the internet, from a host’s remote servers to the user’s device.</p>
        <hr>
        <p>In the past, this hasn’t worked well due to “high latency”. This refers to the delay between making an input (such as shooting a character) and seeing the result (the character being shot).</p>
        <blockquote>
          <p>However, with improved computing power, internet speeds and clever design tricks, cloud gaming is becoming a crowded market, with big tech companies including Google and Amazon joining in, too.</p>
        </blockquote>
        <p>Sony began experimenting with cloud gaming in 2014 with PS Now. This service allows the streaming of older titles, such as PS3 games. And while Sony continues to offer PS Now for the PS5, and at a cheaper price point than Microsoft’s Game Pass, the PS Now is still focused on old games.</p>
        <p>On the other hand, Microsoft is aggressively pushing its new Project xCloud. This service, which comes bundled with the GamePass, allows users to stream certain newer Xbox games directly to their smartphone or tablet, without even needing to own an Xbox console.</p>
        <p>Clearly, Micosoft’s focus is on players, not consoles. Considering the massive impact smartphones are having on who plays games (and how often), Microsoft may be setting itself up to engage a much larger audience than ever before.</p>
        <h2>An evolving market</h2>
        <p>The 2020 console war looks quite different to those of the past, when a single winner often took it all (or at least the majority of it). Think Nintendo in 1990s North America, or Sony’s domination last generation with the PS4.</p>
        
        <p>In light of Microsoft’s shifting approach, we’re now in a situation where two winners will likely take large chunks of different markets, by doing different things.</p>
        
        <p>On one hand, this might help diversify the market and provide greater variety for consumers. On the other, Sony and Microsoft’s divergence might have gamers spending more than ever.

</p>
