    <h2>Using virtual reality could make you a better person in real life</h2>
    <p class="blog-post-meta">November 1, 2020 by <a href="#">Rohan Bedage</a></p>

    <p>If you’ve ever participated in a virtual reality (VR) experience, you might have found yourself navigating the virtual world as an avatar. If you haven’t, you probably recognise the experience from its portrayal in film and on television.</p>
    <p>Popular media has brought us characters like Jake Sully in Avatar, Wade Watts in Ready Player One, and Danny and Karl in the Black Mirror episode Striking Vipers.</p>
    <p>In these examples, the character’s virtual alter-ego is physically different from who they are in the real world. The connection between the real person and their virtual avatar is called “embodiment”. If you have a strong sense of embodiment when using VR, you might feel as if your virtual body is your own biological body.

</p>
    <p>Virtual embodiment provides an opportunity to explore the world from a different point of view. And studies have shown that experiencing new perspectives in the virtual world can alter your behaviour in real life.</p>

<h2>How virtual embodiment works</h2>

<p>Virtual embodiment isn’t entirely new. PC or console role-playing games generate a similar effect, albeit to a lesser extent. VR technology creates a far greater sense of immersion in the virtual world than two-dimensional screen experiences.</p>
<p>That’s because successful 3D virtual environments use more senses, compared with just visual and audio in 2D screen-based technologies. This approach ensures the user is fully engulfed in the synthetic world, which they experience through their virtual avatar.</p>
<p>Immersive visuals in VR trick the user into believing they are elsewhere, such as atop Mount Everest or at the Eiffel Tower. By presenting separate images to each eye, a 3D effect can be achieved when the user incorporates the information from each screen in the VR headset.</p>


<h2>Touch, smell and ‘body ownership’</h2>
<p>The sense of touch is a common form of sensory feedback. Every time you feel your mobile vibrate in your pocket, you’re interacting with “haptic” technology.</p>
<p>In VR, haptic devices simulate physical sensations that are triggered when avatars interact with virtual objects. There are devices that can alter an avatar’s weight distribution or aerodynamics to mimic what is happening in the virtual environment. Real physical props can also be used to introduce real-life challenges to VR sports. Haptic sensations can even be created in mid-air.</p>
<p>Smell, or olfactory sense, is another important mechanism that improves engagement within a virtual world. A Kickstarter campaign for a VR mask that can simulate the sense of smell using aroma capsules has exceeded its funding target, demonstrating the level of interest in multisensory VR.</p>  
<p>In addition to extra senses, VR gives the user a sense of body ownership over the virtual avatar. Body ownership refers to the self-attribution of a (virtual) body. This can be achieved by synchronising multiple sensory feedback.</p>  
<p>For example, when the user can see their virtual hand being touched and can feel the haptic sensation at the same time, they are more likely to believe the virtual body is theirs. This is demonstrated by the famous rubber hand experiment.</p> 

<h2>How virtual bodies affect behaviour</h2>
<p>People respond differently to virtual avatars depending on who they are and the characteristics of the avatar. For example, a recent study found that women dislike their virtual avatar having male hands, whereas men are more likely to accept avatar hands of any gender.</p> 
<p>Another study found that racial bias decreases when caucasians are represented by avatars that have darker compared with lighter skin.</p>
<p>The body shape of the avatar also affects behaviour. Researchers found that game players showed increased physical activity in the real world if they regularly played games with thin avatars as opposed to obese ones.</p> 
<p>This suggests that the identities of virtual avatars can take precedence over our usual identities.</p>


<h2>Choosing the right path</h2>
<p>The ability to embody a virtual avatar blurs the lines between what’s going on in the headset and what’s happening in real life. It feeds the freedom to explore and experiment, whether that’s with a different personality, gender or physicality.</p>  
<p>But the option has to be available in the first place if it’s going to have an impact. PC Gamer reported this week that the developers of the medieval multiplayer game Mordhau were considering introducing female and racially diverse skin tones into the game. The suggestion (which they deny) that they were also planning to give players the option to turn off this diversity if they don’t like it led to a wave of backlash within the gaming community.</p>
<p>Our own research with older adults has also revealed frustrations with the lack of flexibility in avatar creation tools, such as the inability to modify personal characteristics like facial features and fitness levels.</p>
<p>Embodiment is powerful. It can influence your self-identity, perception, and behaviours both in and outside of virtual worlds. The onus is on the future designers and developers of this technology to ensure this power is used for good.</p>  
